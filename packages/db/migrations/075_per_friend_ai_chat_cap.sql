-- Migration 075: 友だち単位の AI 接客レート上限
--
-- 目的:
--   1 人の友だちが 1 日のうちに何度も AI 接客応答を引き出すと、その友だちだけで
--   コストが膨らむ。テナント予算 (monthly_budget_cap_yen) とは別軸で、
--   「1 友だち / 24 時間あたりの課金 AI 応答回数」に上限を設ける。
--
--   - NULL = 無制限
--   - DEFAULT 30 = 通常の接客では十分。荒らし・自動化のような異常利用だけを止める。
--   超過時は自動応答を停止し、スタッフへエスカレ (予算超過時と同じ穏便な定型文)。
--
-- SQLite では定数 DEFAULT 付き ADD COLUMN は既存行にもその値が入る。
-- ============================================================================

ALTER TABLE tenant_metering
  ADD COLUMN per_friend_daily_cap INTEGER DEFAULT 30;
