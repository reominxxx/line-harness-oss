/**
 * Markdown 記号を文字列から強制除去するサニタイザー。
 * AI がプロンプトに従わなかった場合の最終防衛線として、出力直前にかける。
 * - **text** / __text__ → text
 * - *text* / _text_ → text (前後が単語境界の時のみ)
 * - # 見出し → 見出し
 * - `inline` → inline
 * - ```code``` → code
 */
export function stripMarkdown(input: string): string {
  if (!input) return input;
  let out = input;

  // 1. コードブロック ```...``` (改行含む)
  out = out.replace(/```[\s\S]*?```/g, (m) => m.replace(/```/g, ''));

  // 2. 太字 / 斜体 のペア
  out = out.replace(/\*\*([^*\n]+)\*\*/g, '$1');
  out = out.replace(/__([^_\n]+)__/g, '$1');
  // 単一 * / _ は単語強調にしか使われない想定。前後に非空白文字がある場合のみ除去。
  out = out.replace(/(^|\s)\*([^*\n]+)\*(?=\s|$|[、。！？]|$)/g, '$1$2');
  out = out.replace(/(^|\s)_([^_\n]+)_(?=\s|$|[、。！？]|$)/g, '$1$2');

  // 3. インラインコード `text`
  out = out.replace(/`([^`\n]+)`/g, '$1');

  // 4. 見出し # / ## / ### を行頭から除去
  out = out.replace(/^#{1,6}\s+/gm, '');

  // 5. 行頭の箇条書きマーカー "- " "* " "+ " を「・」に変換
  //    LINE 上で見やすくするための置換 (ハイフンや星マークが残ると違和感あり)
  out = out.replace(/^[\-*+]\s+/gm, '・');

  // 6. 引用 "> " を行頭から除去
  out = out.replace(/^>\s+/gm, '');

  // 念のため孤立した残骸 (**, ## 等) を念押しで削除
  out = out.replace(/\*\*+/g, '');
  out = out.replace(/__+/g, '');
  out = out.replace(/^#+\s*/gm, '');

  return out;
}
